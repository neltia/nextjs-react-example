// JavaScript의 기본 7가지 데이터 타입 예시
const num = 42;              // number
const str = 'hello';         // string
const bool = true;           // boolean
const nothing = null;        // object (null)
const notDefined = undefined;// undefined
const sym = Symbol('id');    // symbol

// 각 타입의 typeof 결과를 확인합니다.
console.log(
  typeof num,
  typeof str,
  typeof bool,
  typeof nothing,
  typeof notDefined,
  typeof sym
);